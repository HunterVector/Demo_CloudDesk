import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.PropertySource;

/**
 * <Description> <br> 
 *
 * @author zheng.yangyang<br>
 * @version 1.0<br>
 * @taskId <br>
 * @CreateDate  <br>
 * @since V8.0<br>
 * @see  <br>
 */

@Configuration
@ComponentScan(basePackages = { "com.hunter.demo.cd.fs.provider.*" })
@PropertySource("classpath:SystemParam.properties")
public class FileServerConfig {
    @Value("${upload.path}")
    private String uploadPath;
    
    @Value("${default.file.name}")
    private String fileDefaultName;

    public String getUploadPath() {
        return uploadPath;
    }

    public void setUploadPath(String uploadPath) {
        this.uploadPath = uploadPath;
    }

    public String getFileDefaultName() {
        return fileDefaultName;
    }

    public void setFileDefaultName(String fileDefaultName) {
        this.fileDefaultName = fileDefaultName;
    }
}
